<template>
  <div class="honorific-form">
    <FormWrapper
      @submit="submit"
      @cancel="cancel"
      :loading="loading"
      :honorific="$tc('property.honorific')"
      :error="error"
    >
      <input v-model="honorific.id" readonly />
      <input
        type="text"
        v-model="honorific.name"
        :placeholder="$tc('attribute.name')"
        autofocus
      />
    </FormWrapper>
  </div>
</template>

<script>
import Query from "../../../database/query.js";
import FormWrapper from "../FormWrapper.vue";

export default {
  components: { FormWrapper },
  name: "HonorificForm",
  created: function () {
    let id = this.$route.params.id;
    if (id != null) {
      new Query("honorific")
        .get(id, ["id", "name"])
        .then((result) => {
          this.honorific = result.data.data.getHonorific;
        })
        .catch((err) => {
          this.$data.error = this.$t("error.loading_element");
          console.log(err);
        })
        .finally(() => {
          this.$data.loading = false;
        });
    } else {
      this.$data.loading = false;
    }
  },
  methods: {
    submit: function () {
      new Query("honorific")
        .update(this.honorific)
        .then(() => {
          this.$router.push({
            name: "Property",
            params: { property: "honorific" },
          });
        })
        .catch((err) => {
          this.error = this.$t("error.could_not_update_element");
          console.error(err);
        });
    },
    cancel: function () {
      this.$router.push({ path: "/honorific" });
    },
  },
  data: function () {
    return {
      error: "",
      loading: true,
      honorific: { id: -1, name: "" },
    };
  },
};
</script>
